        Write-Host ""
        Write-Host "  ✅ API keys saved to .env file" -ForegroundColor Green
    } else {
        Write-Host ""
        Write-Host "  No API keys were entered. You can configure them later in Settings." -ForegroundColor Yellow
    }
}

function Invoke-BuildCheck {
    Show-Progress "Verifying build..."
    
    Write-Log "Running build verification" -Level INFO
    
    try {
        $buildCmd = if ($Script:PackageManager -eq "bun") {
            "bun run build"
        } else {
            "npm run build"
        }
        
        $result = Invoke-Expression "$buildCmd 2>&1"
        
        if ($LASTEXITCODE -eq 0) {
            Write-Log "Build completed successfully" -Level SUCCESS
        } else {
            Write-Log "Build output: $result" -Level DEBUG
            $error = Get-ErrorSolution "E009"
            Add-Error "E009" $error.Message $error.Solution -Fatal $false
        }
    } catch {
        Write-Log "Build check failed: $_" -Level WARN
        Add-Warning "W006" "Build verification failed" "Run 'bun run build' manually to check errors"
    }
    
    Show-Progress "Build verified" 1
}

function Invoke-FinalChecks {
    Show-Progress "Running final checks..."
    
    Write-Log "Performing final checks" -Level INFO
    
    # Verify critical files
    $criticalFiles = @(
        "package.json",
        "prisma\schema.prisma",
        "src\app\page.tsx",
        "src\lib\db.ts"
    )
    
    foreach ($file in $criticalFiles) {
        if (-not (Test-Path $file)) {
            Add-Warning "W007" "Missing file: $file" "Verify the repository was cloned correctly"
        }
    }
    
    # Check if database file exists
    if (Test-Path "prisma\dev.db") {
        Write-Log "Database file found" -Level SUCCESS
    } else {
        Add-Warning "W008" "Database file not found" "Run 'npx prisma migrate dev' to create the database"
    }
    
    Show-Progress "Final checks completed" 1
}

function Show-SuccessMessage {
    Write-Header "Setup Complete!"
    
    Write-Host "  $($Config.AppName) v$($Config.Version)" -ForegroundColor Cyan
    Write-Host ""
    Write-Host "  Next Steps:" -ForegroundColor White
    Write-Host "  1. Start the development server:" -ForegroundColor Gray
    Write-Host "     .\start.ps1" -ForegroundColor Green
    Write-Host ""
    Write-Host "  2. Or run manually:" -ForegroundColor Gray
    Write-Host "     bun run dev" -ForegroundColor Green
    Write-Host ""
    Write-Host "  3. Open in browser:" -ForegroundColor Gray
    Write-Host "     http://localhost:3000" -ForegroundColor Green
    Write-Host ""
    Write-Host "  4. Configure API keys in Settings (optional)" -ForegroundColor Gray
    Write-Host ""
    Write-Host "  Documentation: https://github.com/Shootre21/political-spectrum-app-v2" -ForegroundColor DarkGray
    Write-Host "  Log file: $LogFile" -ForegroundColor DarkGray
    Write-Host ""
}

# ============================================
# MAIN EXECUTION
# ============================================
function Main {
    $startTime = Get-Date
    
    # Initialize log
    try {
        "" | Out-File -FilePath $LogFile -Encoding utf8 -Force
    } catch {
        $LogFile = ".\setup-$(Get-Date -Format 'yyyyMMddHHmmss').log"
    }
    
    Write-Header "$($Config.AppName) Setup v$($Config.Version)"
    
    Write-Log "Starting setup process" -Level INFO
    Write-Log "PowerShell version: $($PSVersionTable.PSVersion)" -Level DEBUG
    Write-Log "Working directory: $PWD" -Level DEBUG
    
    # Calculate total steps
    $totalSteps = 6
    if (-not $SkipDeps) { $totalSteps++ }
    if (-not $SkipDB) { $totalSteps++ }
    if (-not $SkipEnv) { $totalSteps++ }
    Reset-Progress -Total $totalSteps
    
    try {
        # Step 1: Prerequisites
        Invoke-PrerequisitesCheck
        
        # Step 2: Dependencies
        if (-not $SkipDeps) {
            Invoke-DependencyInstall
        }
        
        # Step 3: Database
        if (-not $SkipDB) {
            Invoke-DatabaseSetup
        }
        
        # Step 4: Environment
        if (-not $SkipEnv) {
            Invoke-EnvironmentSetup
        }
        
        # Step 5: Credentials prompt
        Invoke-CredentialsPrompt
        
        # Step 6: Build check
        Invoke-BuildCheck
        
        # Step 7: Final checks
        Invoke-FinalChecks
        
        # Show summary
        Show-ErrorSummary
        
        # Show success message if no fatal errors
        if ($Script:Errors | Where-Object { $_.Fatal }) {
            Write-Host "`n  Setup completed with errors. Please fix the issues above." -ForegroundColor Yellow
        } else {
            Show-SuccessMessage
        }
        
    } catch {
        Write-Host "`n  Setup failed: $_" -ForegroundColor Red
        Write-Log "Setup failed: $_" -Level ERROR
        Show-ErrorSummary
        exit 1
    }
    
    $duration = ((Get-Date) - $startTime).TotalSeconds
    Write-Log "Setup completed in $([Math]::Round($duration, 1)) seconds" -Level INFO
    
    # Write summary to log
    $summary = @"

========================================
SETUP SUMMARY
========================================
Timestamp: $(Get-Date)
Duration: $([Math]::Round($duration, 1)) seconds
Errors: $($Script:Errors.Count)
Warnings: $($Script:Warnings.Count)
Log File: $LogFile
========================================
"@
    
    Add-Content -Path $LogFile -Value $summary
}

# Run main function
Main
