    $geminiKey = Read-Host
    if ($geminiKey) { $apiKeys.GEMINI_API_KEY = $geminiKey }
    
    # Grok
    Write-Host "  xAI Grok API Key: " -NoNewline -ForegroundColor White
    $grokKey = Read-Host
    if ($grokKey) { $apiKeys.GROK_API_KEY = $grokKey }
    
    # Kimi
    Write-Host "  Kimi/Moonshot API Key: " -NoNewline -ForegroundColor White
    $kimiKey = Read-Host
    if ($kimiKey) { $apiKeys.KIMI_API_KEY = $kimiKey }
    
    # Z.ai
    Write-Host "  Z.ai API Key: " -NoNewline -ForegroundColor White
    $zaiKey = Read-Host
    if ($zaiKey) { $apiKeys.ZAI_API_KEY = $zaiKey }
    
    # Update .env file
    if ($apiKeys.Count -gt 0) {
        Write-Host ""
        Write-Host "  Updating .env file..." -ForegroundColor Cyan
        
        $envContent = Get-Content ".env" -Raw -ErrorAction SilentlyContinue
        
        foreach ($key in $apiKeys.Keys) {
            $value = $apiKeys[$key]
            if ($envContent -match "$key=.*") {
                $envContent = $envContent -replace "$key=.*", "$key=`"$value`""
            } else {
                $envContent += "`n$key=`"$value`""
            }
