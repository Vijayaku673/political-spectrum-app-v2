function Write-Log {
    param(
        [string]$Message,
        [ValidateSet('INFO', 'WARN', 'ERROR', 'SUCCESS', 'DEBUG')]
        [string]$Level = 'INFO'
    )
    
    $timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
    $logEntry = "[$timestamp] [$Level] $Message"
    
    # Write to log file
    try {
        Add-Content -Path $LogFile -Value $logEntry -ErrorAction SilentlyContinue
    } catch {}
    
    # Console output with colors
    switch ($Level) {
        'INFO'    { Write-Host $logEntry -ForegroundColor Cyan }
        'WARN'    { Write-Host $logEntry -ForegroundColor Yellow }
        'ERROR'   { Write-Host $logEntry -ForegroundColor Red }
        'SUCCESS' { Write-Host $logEntry -ForegroundColor Green }
        'DEBUG'   { if ($Verbose) { Write-Host $logEntry -ForegroundColor Gray } }
    }
}

function Write-Header {
    param([string]$Title)
    
    $width = 60
    $padding = [Math]::Max(0, ($width - $Title.Length) / 2)
    
    Write-Host ""
    Write-Host ("=" * $width) -ForegroundColor DarkCyan
    Write-Host (" " * [Math]::Floor($padding) + $Title) -ForegroundColor Cyan
    Write-Host ("=" * $width) -ForegroundColor DarkCyan
    Write-Host ""
}

# ============================================
# PROGRESS BAR SYSTEM
# ============================================
$Script:Progress = @{
    Current = 0
    Total = 10
    Activity = "Setting up Political Spectrum App"
}

function Show-Progress {
    param(
        [string]$Status,
        [int]$Increment = 1
    )
    
    $Script:Progress.Current += $Increment
    $percent = [Math]::Min(100, [Math]::Round(($Script:Progress.Current / $Script:Progress.Total) * 100))
    
    # Create progress bar string
    $barWidth = 30
    $filled = [Math]::Round($barWidth * ($percent / 100))
    $empty = $barWidth - $filled
    $bar = "[" + ("=" * $filled) + (" " * $empty) + "]"
    
    Write-Host "`r$bar $percent% - $Status" -NoNewline -ForegroundColor Yellow
    
    if ($Script:Progress.Current -ge $Script:Progress.Total) {
        Write-Host ""
    }
}

function Reset-Progress {
    param([int]$Total = 10)
    $Script:Progress.Current = 0
    $Script:Progress.Total = $Total
}

# ============================================
# ERROR HANDLING SYSTEM
# ============================================
$Script:Errors = @()
$Script:Warnings = @()

function Add-Error {
    param(
        [string]$Code,
        [string]$Message,
        [string]$Solution,
        [bool]$Fatal = $false
    )
    
    $errorObj = @{
        Code = $Code
        Message = $Message
        Solution = $Solution
        Fatal = $Fatal
        Timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
    }
    
    $Script:Errors += $errorObj
    Write-Log $Message -Level ERROR
    
    if ($Fatal) {
        Show-ErrorSummary
        throw "Fatal error occurred: $Message"
    }
}

function Add-Warning {
    param(
        [string]$Code,
        [string]$Message,
        [string]$Solution
    )
    
    $warningObj = @{
        Code = $Code
        Message = $Message
        Solution = $Solution
        Timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
    }
    
    $Script:Warnings += $warningObj
    Write-Log $Message -Level WARN
}

function Show-ErrorSummary {
    Write-Header "Setup Summary"
    
    if ($Script:Errors.Count -eq 0 -and $Script:Warnings.Count -eq 0) {
        Write-Host "  [OK] No errors or warnings encountered!" -ForegroundColor Green
        return
    }
    
    if ($Script:Errors.Count -gt 0) {
        Write-Host "`n  ERRORS ($($Script:Errors.Count)):" -ForegroundColor Red
        foreach ($err in $Script:Errors) {
            Write-Host "    [$($err.Code)] $($err.Message)" -ForegroundColor Red
            Write-Host "           Solution: $($err.Solution)" -ForegroundColor Yellow
        }
    }
    
    if ($Script:Warnings.Count -gt 0) {
        Write-Host "`n  WARNINGS ($($Script:Warnings.Count)):" -ForegroundColor Yellow
        foreach ($warn in $Script:Warnings) {
            Write-Host "    [$($warn.Code)] $($warn.Message)" -ForegroundColor Yellow
            Write-Host "           Solution: $($warn.Solution)" -ForegroundColor Cyan
        }
    }
    
    Write-Host ""
}

