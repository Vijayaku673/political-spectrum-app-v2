function Test-Administrator {
    $currentUser = [Security.Principal.WindowsIdentity]::GetCurrent()
    $principal = New-Object Security.Principal.WindowsPrincipal($currentUser)
    return $principal.IsInRole([Security.Principal.WindowsBuiltInRole]::Administrator)
}

function Test-CommandExists {
    param([string]$Command)
    return $null -ne (Get-Command $Command -ErrorAction SilentlyContinue)
}

function Test-PortAvailable {
    param([int]$Port)
    $connection = Get-NetTCPConnection -LocalPort $Port -ErrorAction SilentlyContinue
    return $null -eq $connection
}

function Get-NodeVersion {
    if (Test-CommandExists "node") {
        $version = node --version
        return $version -replace 'v', ''
    }
    return $null
}

function Compare-Versions {
    param([string]$V1, [string]$V2)
    
    $v1Parts = $V1.Split('.')
    $v2Parts = $V2.Split('.')
    
    for ($i = 0; $i -lt [Math]::Max($v1Parts.Length, $v2Parts.Length); $i++) {
        $v1Num = if ($i -lt $v1Parts.Length) { [int]$v1Parts[$i] } else { 0 }
        $v2Num = if ($i -lt $v2Parts.Length) { [int]$v2Parts[$i] } else { 0 }
        
        if ($v1Num -gt $v2Num) { return 1 }
        if ($v1Num -lt $v2Num) { return -1 }
    }
    return 0
}

function Test-DiskSpace {
    param([string]$Path, [long]$RequiredMB = 500)
    
    try {
        $drive = (Get-Item $Path -ErrorAction SilentlyContinue).PSDrive
        if (-not $drive) {
            $drive = Get-PSDrive -Name $Path.Substring(0,1)
        }
        $freeSpaceMB = [Math]::Round($drive.Free / 1MB, 2)
        return $freeSpaceMB -ge $RequiredMB
    } catch {
        return $true  # Assume OK if can't check
    }
}

