function Invoke-PrerequisitesCheck {
    Show-Progress "Checking prerequisites..."
    
    Write-Log "Starting prerequisites check" -Level INFO
    
    # Check if running as admin (warn but don't fail)
    if (-not (Test-Administrator)) {
        Add-Warning "W001" "Not running as Administrator" "Some features may require elevated permissions"
    }
    
    # Check disk space
    if (-not (Test-DiskSpace $PWD)) {
        $error = Get-ErrorSolution "E011"
        Add-Error "E011" $error.Message $error.Solution -Fatal $false
    }
    
    # Check Node.js
    $nodeVersion = Get-NodeVersion
    if (-not $nodeVersion) {
        $error = Get-ErrorSolution "E001"
        Add-Error "E001" $error.Message $error.Solution -Fatal $true
    }
    
    if ((Compare-Versions $nodeVersion $Config.NodeMinVersion) -lt 0) {
        $error = Get-ErrorSolution "E002"
        Add-Error "E002" "$($error.Message) (Current: $nodeVersion, Required: $($Config.NodeMinVersion))" $error.Solution -Fatal $true
    }
    
    Write-Log "Node.js version: $nodeVersion" -Level SUCCESS
    
    # Check Bun (preferred) or npm
    if (-not (Test-CommandExists "bun")) {
        Add-Warning "W002" "Bun is not installed (recommended)" "Install from https://bun.sh for faster performance"
        
        if (-not (Test-CommandExists "npm")) {
            $error = Get-ErrorSolution "E003"
            Add-Error "E003" "Neither Bun nor npm is available" $error.Solution -Fatal $true
        }
        
        $Script:PackageManager = "npm"
    } else {
        $Script:PackageManager = "bun"
        Write-Log "Using Bun as package manager" -Level SUCCESS
    }
    
    # Check Git
    if (-not (Test-CommandExists "git")) {
        Add-Warning "W003" "Git is not installed" "Required for version control: winget install Git.Git"
    }
    
    # Check ports
    foreach ($port in $Config.RequiredPorts) {
        if (-not (Test-PortAvailable $port)) {
            $error = Get-ErrorSolution "E004"
            Add-Warning "W004" "Port $port is already in use" $error.Solution
        }
    }
    
    Show-Progress "Prerequisites checked" 1
    return $true
}

function Invoke-DependencyInstall {
    Show-Progress "Installing dependencies..."
    
    Write-Log "Starting dependency installation" -Level INFO
    
    # Clean install if Force flag or node_modules missing
    if ($Force -or -not (Test-Path "node_modules")) {
        if (Test-Path "node_modules") {
            Write-Log "Removing existing node_modules..." -Level INFO
            Remove-Item "node_modules" -Recurse -Force -ErrorAction SilentlyContinue
        }
    }
    
    # Install dependencies
    $installCmd = if ($Script:PackageManager -eq "bun") {
        "bun install"
    } else {
        "npm install"
    }
    
    Write-Log "Running: $installCmd" -Level DEBUG
    
    try {
        $result = Invoke-Expression "$installCmd 2>&1"
        if ($LASTEXITCODE -ne 0) {
            $error = Get-ErrorSolution "E005"
            Add-Error "E005" $error.Message $error.Solution -Fatal $false
            Write-Log "Install output: $result" -Level DEBUG
            
            # Retry with npm if bun failed
            if ($Script:PackageManager -eq "bun") {
                Write-Log "Retrying with npm..." -Level WARN
                npm install
                if ($LASTEXITCODE -ne 0) {
                    Add-Error "E005" "Package installation failed after retry" $error.Solution -Fatal $true
                }
            }
        }
        
        Write-Log "Dependencies installed successfully" -Level SUCCESS
    } catch {
        $error = Get-ErrorSolution "E005"
        Add-Error "E005" "$($error.Message): $_" $error.Solution -Fatal $true
    }
    
    Show-Progress "Dependencies installed" 1
}

function Invoke-DatabaseSetup {
    Show-Progress "Setting up database..."
    
    Write-Log "Starting database setup" -Level INFO
    
    # Ensure prisma is available
    if (-not (Test-Path "node_modules\.bin\prisma") -and -not (Test-CommandExists "prisma")) {
        Add-Warning "W005" "Prisma CLI not found" "Installing Prisma..."
        
        if ($Script:PackageManager -eq "bun") {
            bun add prisma @prisma/client
        } else {
            npm install prisma @prisma/client
        }
    }
    
    # Generate Prisma client
    Write-Log "Generating Prisma client..." -Level INFO
    try {
        npx prisma generate 2>&1 | Out-Null
        Write-Log "Prisma client generated" -Level SUCCESS
    } catch {
        $error = Get-ErrorSolution "E007"
        Add-Error "E007" $error.Message $error.Solution -Fatal $false
    }
    
    # Run migrations
    Write-Log "Running database migrations..." -Level INFO
    
    if (-not (Test-Path "prisma\dev.db")) {
        try {
            npx prisma migrate dev --name init 2>&1 | Out-Null
            Write-Log "Database migrations completed" -Level SUCCESS
        } catch {
            # Try push if migrate fails
            Write-Log "Migration failed, trying db push..." -Level WARN
            try {
                npx prisma db push 2>&1 | Out-Null
                Write-Log "Database schema pushed successfully" -Level SUCCESS
            } catch {
                $error = Get-ErrorSolution "E006"
                Add-Error "E006" $error.Message $error.Solution -Fatal $false
            }
        }
    } else {
        Write-Log "Database already exists, skipping migration" -Level INFO
    }
    
    Show-Progress "Database configured" 1
}

