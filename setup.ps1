function Write-Log {
    param(
        [string]$Message,
        [ValidateSet('INFO', 'WARN', 'ERROR', 'SUCCESS', 'DEBUG')]
        [string]$Level = 'INFO'
    )
    
    $timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
    $logEntry = "[$timestamp] [$Level] $Message"
    
    # Write to log file
    try {
        Add-Content -Path $LogFile -Value $logEntry -ErrorAction SilentlyContinue
    } catch {}
    
    # Console output with colors
    switch ($Level) {
        'INFO'    { Write-Host $logEntry -ForegroundColor Cyan }
        'WARN'    { Write-Host $logEntry -ForegroundColor Yellow }
        'ERROR'   { Write-Host $logEntry -ForegroundColor Red }
        'SUCCESS' { Write-Host $logEntry -ForegroundColor Green }
        'DEBUG'   { if ($Verbose) { Write-Host $logEntry -ForegroundColor Gray } }
    }
}

function Write-Header {
    param([string]$Title)
    
    $width = 60
    $padding = [Math]::Max(0, ($width - $Title.Length) / 2)
    
    Write-Host ""
    Write-Host ("=" * $width) -ForegroundColor DarkCyan
    Write-Host (" " * [Math]::Floor($padding) + $Title) -ForegroundColor Cyan
    Write-Host ("=" * $width) -ForegroundColor DarkCyan
    Write-Host ""
}

# ============================================
# PROGRESS BAR SYSTEM
# ============================================
$Script:Progress = @{
    Current = 0
    Total = 10
    Activity = "Setting up Political Spectrum App"
}

function Show-Progress {
    param(
        [string]$Status,
        [int]$Increment = 1
    )
    
    $Script:Progress.Current += $Increment
    $percent = [Math]::Min(100, [Math]::Round(($Script:Progress.Current / $Script:Progress.Total) * 100))
    
    # Create progress bar string
    $barWidth = 30
    $filled = [Math]::Round($barWidth * ($percent / 100))
    $empty = $barWidth - $filled
    $bar = "[" + ("=" * $filled) + (" " * $empty) + "]"
    
    Write-Host "`r$bar $percent% - $Status" -NoNewline -ForegroundColor Yellow
    
    if ($Script:Progress.Current -ge $Script:Progress.Total) {
        Write-Host ""
    }
}

function Reset-Progress {
    param([int]$Total = 10)
    $Script:Progress.Current = 0
    $Script:Progress.Total = $Total
}

