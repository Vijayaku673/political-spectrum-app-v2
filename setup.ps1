function Main {
    $startTime = Get-Date
    
    # Initialize log
    try {
        "" | Out-File -FilePath $LogFile -Encoding utf8 -Force
    } catch {
        $LogFile = ".\setup-$(Get-Date -Format 'yyyyMMddHHmmss').log"
    }
    
    Write-Header "$($Config.AppName) Setup v$($Config.Version)"
    
    Write-Log "Starting setup process" -Level INFO
    Write-Log "PowerShell version: $($PSVersionTable.PSVersion)" -Level DEBUG
    Write-Log "Working directory: $PWD" -Level DEBUG
    
    # Calculate total steps
    $totalSteps = 7
    if (-not $SkipDeps) { $totalSteps++ }
    if (-not $SkipDB) { $totalSteps++ }
    if (-not $SkipEnv) { $totalSteps++ }
    Reset-Progress -Total $totalSteps
    
    try {
        # Step 1: Prerequisites
        Invoke-PrerequisitesCheck
        
        # Step 2: Dependencies
        if (-not $SkipDeps) {
            Invoke-DependencyInstall
        }
        
        # Step 3: Database
        if (-not $SkipDB) {
            Invoke-DatabaseSetup
        }
        
        # Step 4: Environment
        if (-not $SkipEnv) {
            Invoke-EnvironmentSetup
        }
        
        # Step 5: Credentials prompt
        Invoke-CredentialsPrompt
        
        # Step 6: Build check
        Invoke-BuildCheck
        
        # Step 7: Final checks
        Invoke-FinalChecks
        
        # Show summary
        Show-ErrorSummary
        
        # Show success message if no fatal errors
        if ($Script:Errors | Where-Object { $_.Fatal }) {
            Write-Host "`n  Setup completed with errors. Please fix the issues above." -ForegroundColor Yellow
        } else {
            Show-SuccessMessage
        }
        
    } catch {
        Write-Host "`n  Setup failed: $_" -ForegroundColor Red
        Write-Log "Setup failed: $_" -Level ERROR
        Show-ErrorSummary
        exit 1
    }
    
    $duration = ((Get-Date) - $startTime).TotalSeconds
    Write-Log "Setup completed in $([Math]::Round($duration, 1)) seconds" -Level INFO
    
    # Write summary to log
    $summary = @"

========================================
SETUP SUMMARY
========================================
Timestamp: $(Get-Date)
Duration: $([Math]::Round($duration, 1)) seconds
Errors: $($Script:Errors.Count)
Warnings: $($Script:Warnings.Count)
Log File: $LogFile
========================================
"@
    
    Add-Content -Path $LogFile -Value $summary
    
    # Start server if requested or prompt user
    if (-not ($Script:Errors | Where-Object { $_.Fatal })) {
        if ($Start) {
            # Auto-start requested
            Start-DevServer -WithMonitor
        } elseif (-not $NoMonitor) {
            # Ask user if they want to start
            Write-Host ""
            Write-Host "  ============================================================" -ForegroundColor Cyan
            Write-Host "  Would you like to start the development server now?" -ForegroundColor White
            Write-Host "  [Y] Yes, start server  [N] No, exit" -ForegroundColor Gray
            Write-Host "  ============================================================" -ForegroundColor Cyan
            Write-Host ""
            
            # Use Read-Host for reliable input
            $response = Read-Host "  Enter Y or N"
            
            if ($response -eq 'Y' -or $response -eq 'y') {
                Write-Host ""
                Start-DevServer -WithMonitor
            } else {
                Write-Host ""
                Write-Host "  To start the server later, run:" -ForegroundColor Gray
                Write-Host "  .\start.ps1" -ForegroundColor Green
                Write-Host "  or: bun run dev" -ForegroundColor Green
                Write-Host ""
                Write-Host "  Then open: http://localhost:3000" -ForegroundColor Cyan
                Write-Host ""
            }
        }
    }
}

# Run main function
Main
